public class User {

    private String nickname;
    private String name;
    private String surname;
    private String mail;
    private String key;

    public String getNickname() {
        return nickname;
    }

    public String getName() {
        return name;
    }

    public String getSurname() {
        return surname;
    }

    public String getMail() {
        return mail;
    }

    public String getKey() {
        return key;
    }
}
